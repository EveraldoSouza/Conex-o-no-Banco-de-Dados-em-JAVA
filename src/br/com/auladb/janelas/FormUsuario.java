private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    
    Usuario usu = new Usuario(0, TextNome.getText(), TextCPF.getText(), TextLogin.getText(), TextSenha.getText());
     
    
    
    if(TextNome.getText().isEmpty() || TextCPF.getText().isEmpty() || TextLogin.getText().isEmpty() || TextSenha.getText().isEmpty()){
        JOptionPane.showMessageDialog(null, "FAVOR, NÃO DEIXAR CAIXAS VAZIAS");
    }   else if (new VUsuario().cadastrar(usu)){
        JOptionPane.showMessageDialog(null, "CADASTRADO COM SUCESSO");
    } else {
        JOptionPane.showMessageDialog(null, "LOGIN JA EXISTENTE");
    }
}//GEN-LAST:event_jButton1ActionPerformed

