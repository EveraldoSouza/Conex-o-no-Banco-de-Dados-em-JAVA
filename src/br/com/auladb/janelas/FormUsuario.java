    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        Usuario usu = new Usuario(Integer.valueOf(TextID.getText()), TextNome.getText(),TextCPF.getText(),TextLogin.getText(), TextSenha.getText());
        
        if(new VUsuario().consultar(usu)){
            TextNome.setText(usu.getNome());
            TextCPF.setText(usu.getCPF());
            TextLogin.setText(usu.getLogin());
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

