    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       
        Produto prod = new Produto("", Integer.valueOf(TextId.getText()), 0, 0);
        

        if(TextId.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "VALOR DE ID VAZIO");
        } 
        else if(new VProduto().consultar(prod)){
            
            TextDescricao.setText(prod.getDescricao());
            TextQuantidade.setText(String.valueOf(prod.getQuantidade()));
            TextValor.setText(String.valueOf(prod.getValor()));
        
        }
    }//GEN-LAST:event_jButton3ActionPerformed

