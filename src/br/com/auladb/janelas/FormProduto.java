    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        Produto prod = new Produto (TextDescricao.getText(), 0, Integer.valueOf(TextQuantidade.getText()), Double.valueOf(TextValor.getText()));
        
        if(new VProduto().cadastrar(prod)){
            JOptionPane.showMessageDialog(null, "PRODUTO CADASTRADO COM SUCESSO.");
        }else{
            JOptionPane.showMessageDialog(null, "PRODUTO JA ESTA CADASTRADO.");
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

